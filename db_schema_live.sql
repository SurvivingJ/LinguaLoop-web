-- ---------------------------------------------------------------------------
-- tests_containing_sense — Find tests that include a given word sense
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.tests_containing_sense(
    p_sense_id    INTEGER,
    p_language_id INTEGER
)
RETURNS TABLE (
    id              UUID,
    transcript      TEXT,
    difficulty      NUMERIC,
    vocab_token_map JSONB
)
LANGUAGE sql STABLE
AS $$
    SELECT t.id, t.transcript, t.difficulty, t.vocab_token_map
    FROM public.tests t
    WHERE t.vocab_sense_ids @> ARRAY[p_sense_id]
      AND t.language_id = p_language_id
      AND t.is_active = TRUE;
$$;

-- ---------------------------------------------------------------------------
-- get_cefr_config — CEFR level config for a given difficulty
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_cefr_config(p_difficulty INTEGER)
RETURNS TABLE (
    id INTEGER,
    cefr_code TEXT,
    word_count_min INTEGER,
    word_count_max INTEGER,
    initial_elo INTEGER
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT
        c.id::INTEGER,
        c.cefr_code::TEXT,
        c.word_count_min,
        c.word_count_max,
        c.initial_elo
    FROM public.dim_cefr_levels c
