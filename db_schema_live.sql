-- ---------------------------------------------------------------------------
-- get_word_quiz_candidates — Select words for quiz based on BKT uncertainty
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_word_quiz_candidates(
    p_user_id UUID,
    p_sense_ids INTEGER[],
    p_language_id SMALLINT,
    p_max_words INTEGER DEFAULT 5
)
RETURNS TABLE(
    out_sense_id INTEGER,
    out_lemma TEXT,
    out_definition TEXT,
    out_pronunciation TEXT,
    out_p_known NUMERIC,
    out_score NUMERIC
) AS $$
BEGIN
    RETURN QUERY
    SELECT
        uvk.sense_id,
        dv.lemma,
        dws.definition,
        dws.pronunciation,
        uvk.p_known,
        (uvk.p_known * (1 - uvk.p_known) *
         (1.0 / GREATEST(1.0, ln(GREATEST(1.0, COALESCE(dv.frequency_rank, 1.0)::numeric))))
        ) AS score
    FROM public.user_vocabulary_knowledge uvk
    JOIN public.dim_word_senses dws ON dws.id = uvk.sense_id
    JOIN public.dim_vocabulary dv ON dv.id = dws.vocab_id
    WHERE uvk.user_id = p_user_id
      AND uvk.sense_id = ANY(p_sense_ids)
      AND uvk.p_known BETWEEN 0.25 AND 0.75
      AND uvk.status != 'user_marked_unknown'
    ORDER BY score DESC
    LIMIT p_max_words;
END;
$$ LANGUAGE plpgsql;

-- ---------------------------------------------------------------------------
-- get_distractors — Random distractors for word quiz
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_distractors(
    p_sense_id INTEGER,
    p_language_id SMALLINT,
    p_count INTEGER DEFAULT 3
)
RETURNS TABLE(out_definition TEXT) AS $$
BEGIN
    RETURN QUERY
    SELECT dws.definition
    FROM public.dim_word_senses dws
    JOIN public.dim_vocabulary dv ON dv.id = dws.vocab_id
    WHERE dv.language_id = p_language_id
      AND dws.id != p_sense_id
      AND dws.vocab_id != (SELECT vocab_id FROM public.dim_word_senses WHERE id = p_sense_id)
      AND dws.sense_rank = 1
    ORDER BY random()
    LIMIT p_count;
END;
$$ LANGUAGE plpgsql;

